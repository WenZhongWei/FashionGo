package com.maifeng.fashiongo.base;

public class ShoppingcarData {

	private String	id; //购物车id            
	private	String	goodsName;//商品名称
	private	String	goodsImage;//商品图片 
	private	String	goodsCode;//商品code
	private	String	price;//优惠价
	private	String	number;//数量
	private	String	specificationsId;//参数id
	private	String	model;//型号（颜色等等）
	private String	size;//尺码
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public String getGoodsImage() {
		return goodsImage;
	}
	public void setGoodsImage(String goodsImage) {
		this.goodsImage = goodsImage;
	}
	public String getGoodsCode() {
		return goodsCode;
	}
	public void setGoodsCode(String goodsCode) {
		this.goodsCode = goodsCode;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getSpecificationsId() {
		return specificationsId;
	}
	public void setSpecificationsId(String specificationsId) {
		this.specificationsId = specificationsId;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	

	

	
	
	
	
	
}
